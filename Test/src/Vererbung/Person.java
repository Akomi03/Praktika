package Vererbung;

public class Person {
    private String vorname;
    private String nachname;

    public String getVorname() {
        return vorname;
    }

    public void setVorname(String pVorname) {
        vorname = pVorname;
    }

    public String getNachname() {
        return nachname;
    }

    public void setNachname(String pNachname) {
        nachname = pNachname;
    }
}
